import numpy as np

def linear_regression_numpy(x, y):
    """
    使用 NumPy 求解线性回归 y = -a*x + b
    返回 a, b 参数
    """
    # 构建设计矩阵 A = [[-x, 1]] 对应 y = -a*x + b
    A = np.column_stack([-x, np.ones(len(x))])
    
    # 使用最小二乘法求解
    params, residuals, rank, s = np.linalg.lstsq(A, y, rcond=None)
    
    a, b = params
    return a, b

